   !------------------------------------------------------------------------------
   !  Use Bolton's approximation to compute dewpoint.
   !------------------------------------------------------------------------------

   td = t_kelvin + (243.5_r8 / ((17.67_r8 / log(e_mb/6.112_r8)) - 1.0_r8) )
elsewhere
   td = missing_r8
end where

end subroutine get_expected_dew_point

